'''
연산자
'''


def main():
    x = 2
    y = 3

    print('x+y={}'.format(x + y))
    print('x-y={}'.format(x - y))
    print('x*y={}'.format(x * y))
    print('x/y={}'.format(x / y))
    print('x%y={}'.format(x % y))

    # java에 없는 연산자
    # x에 y제곱
    print("x**y={}".format(x**y))

    x=7
    y=4
    # 몫을 구하는 연산자
    print("x//y={}".format(x//y))
main()
